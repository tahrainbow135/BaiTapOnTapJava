package BaiTapOnTap.DongPhuc;

public class DangKy {
    private String maSV;
    private String sizeAo;

    public DangKy(String maSV, String sizeAo) {
        this.maSV = maSV;
        this.sizeAo = sizeAo;
    }

    public String getMaSV() {
        return maSV;
    }

    public String getSizeAo() {
        return sizeAo;
    }
}